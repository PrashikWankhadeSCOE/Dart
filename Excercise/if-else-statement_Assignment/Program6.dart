void main() {
  var num = 4;
  if (num == 1) {
    print("One");
  } else if (num == 2) {
    print("Two");
  } else if (num == 3) {
    print("three");
  } else if (num == 4) {
    print("four");
  } else if (num == 5) {
    print("Five");
  } else {
    print("Entered number is greater than 5");
  }
}
